# Задание-3:
# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

filename = "less4.txt"
with open(filename, "wt", encoding="UTF-8") as less4:
    less4.write("".join([str(random.randint(0, 9)) for _ in range(2500)]))
with open(filename, "rt", encoding="UTF-8") as less4:
    data = less4.read()
    longest = ""
    current = ""
    for _i, char in enumerate(data[:-1]):
        if len(current) > 0 and current[0] == char:
            current += char
        else:
            current = char
        if len(longest) < len(current):
            longest = current
    print(longest)
